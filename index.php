<h2 id="sobre" class="text-center">SOBRE</h2>

<br><br>

<h4 class="text-center">
 O aplicativo Higia, veio para auxiliar a todas as pessoas que precisam de um profissional da área da saúde, seja ele um educador físico, fisioterapeuta, professor de dança, artes marciais, entre outros, que possam auxiliar na evolução e ou manutenção da saúde física e mental. Nos dias atuais, nota-se a dificuldade em encontrar tempo e disposição para cuidar da saúde. Visando encurtar a distância e intermediar o acesso entre profissional e cliente, de maneira prática e efetiva, gerando trabalho e saúde para ambos. O Higia, ainda proporciona aos profissionais que precisam de maior divulgação e ascensão do seu trabalho, pois, com este aplicativo, o portfólio do profissional estará disponível para milhares de potenciais clientes, através de filtros que buscam igualdade entre características básicas, précadastrados por ambos usuários. 
</h4>

