<h2 class="text-center">Como funciona</h2>

<br><br>


  <h4 class="text-center">
  Profissionais como: educadores físicos, fisioterapeutas, professores (as) de dança, professores (as) de artes marciais, entre outros, por exemplo,
  terão a oportunidade de utilizar o aplicativo, como uma alavanca para seu portfólio profissional.
  Tendo a chance de mostrar seu trabalho e encontrar clientes que buscam por alguém com as características e formações especificas que este, tem a oferecer. 
  O cadastro deste profissional, deverá conter todas as suas informações básicas, como nome, data de nascimento, sexo, endereço, área geológica de atuação,
  documento de identificação, formação acadêmica, experiência profissional, especialidade, redes sociais, contatos, foto, etc. 
  O aplicativo irá trabalhar o marketing deste profissional, apresentando-o a milhares de potenciais clientes.
  </h4>
  <br>
  <h4 class="text-center">
  Se tratando de pessoas comuns, o Higia, trabalhará, trazendo a este, o profissional ideal para sua procura. 
  O(a) cliente deverá cadastrar informações como: nome, data de nascimento, sexo, endereço, documento de identificação, redes sociais, contatos, foto,
  preferência por sexo do profissional que irá assisti-lo, área onde pretende receber a assistência (academia, casa, ar livre),
  característica da atividade que deseja treinar e principalmente quais as características esta pessoa busca em um profissional.   
  </h4>

